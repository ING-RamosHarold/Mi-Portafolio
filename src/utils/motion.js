export const fadeIn = (direction = "up", delay = 0.1) => {
  return {
    hidden: {
      opacity: 0,
      y: direction === "up" ? 40 : 0,
    },
    visible: {
      opacity: 1,
      y: 0,
      transition: {
        duration: 0.8, // Más suave
        delay,
        ease: "easeInOut",
      },
    },
  };
};

